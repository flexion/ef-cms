import { PETITION_TYPES } from '@web-client/presenter/actions/setupPetitionStateAction';
import { getCaseCaptionMeta } from '@shared/business/utilities/getCaseCaptionMeta';

export const generatePetitionPdfAction = async ({
  applicationContext,
  props,
}: ActionProps) => {
  const { petitionType } = props.step1Data;

  if (petitionType === PETITION_TYPES.autoGenerated) {
    const petitionInfo = {
      ...props.step1Data,
      ...props.step2Data,
      ...props.step3Data,
      ...props.step4Data,
      ...props.step5Data,
    };

    const caseCaption =
      applicationContext
        .getUtilities()
        .getCaseCaption({ ...petitionInfo, petitioners: [] }) || '';

    const { caseCaptionExtension, caseTitle } = getCaseCaptionMeta({
      caseCaption,
    });

    const docketNumberWithSuffix = '9999-99';

    const {
      caseType,
      contactPrimary,
      contactSecondary,
      petitionFacts,
      petitionReasons,
      preferredTrialCity,
      procedureType,
    } = petitionInfo;

    // This will either be duplicated when we actually create the case, or it should be moved
    const user = applicationContext.getCurrentUser();
    contactPrimary.email = user.email;

    // Why aren't these values set?
    const noticeIssuedDate = '01/01/99';
    const taxYear = '1999';

    //handle business contact types

    const { url } = await applicationContext
      .getUseCases()
      .generatePetitionPdfInteractor(applicationContext, {
        caseCaptionExtension,
        caseTitle,
        caseType,
        contactPrimary,
        contactSecondary,
        docketNumberWithSuffix,
        noticeIssuedDate,
        petitionFacts,
        petitionReasons,
        preferredTrialCity,
        procedureType,
        taxYear,
      });

    return { pdfUrl: url };
  }
};
